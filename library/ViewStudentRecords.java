    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        int rows=model.getRowCount();
        if(rows>0){
            for(int i=0;i<rows;i++){
                model.removeRow(0);
            }
        }

        try{
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            Connection con=(Connection)
            DriverManager.getConnection("jdbc:mysql://localhost:3306/library","root","dps123");
            Statement stmt=con.createStatement();
            ResultSet rs=stmt.executeQuery("Select * from student");
            while (rs.next()){
                String nadmno=rs.getString("admno");
                String nsname=rs.getString("sname");
                String nsclass=rs.getString("sclass");
                String nsec=rs.getString("section");
                model.addRow(new Object[] {nadmno,nsname,nsclass,nsec});
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(this,e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

